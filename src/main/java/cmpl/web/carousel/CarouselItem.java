package cmpl.web.carousel;

/**
 * Object pour les carousel d'image
 * 
 * @author Louis
 *
 */
public class CarouselItem {

  String src;
  String alt;

  public String getSrc() {
    return src;
  }

  public void setSrc(String src) {
    this.src = src;
  }

  public String getAlt() {
    return alt;
  }

  public void setAlt(String alt) {
    this.alt = alt;
  }

}
